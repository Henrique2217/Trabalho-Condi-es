public class atividade03 {
    public static void main(String[] args) {

        //Crie uma variável que armazene um número inteiro
        int numero = 4;

        //Verifique se o número é par ou ímpar
        if (numero % 2 == 0) {

            // O operador de módulo (%) retorna o resto da divisão. Se o resto é 0, o número é par.
            System.out.println("O número é par.");
        } else {

            // Se o resto não é 0, o número é ímpar.
            System.out.println("O número é ímpar.");
        }
    }
}

